package AssignmentThree;

public class Wheels {

    float tirePressure;
    int size;

    public Wheels(float tirePressure, int size) {
        this.tirePressure = tirePressure;
        this.size = size;
    }

    public float getTirePressure() {
        return tirePressure;
    }

    public int getSize() {
        return size;
    }




}
